namespace Magidesk.Domain.Enumerations;

/// <summary>
/// Enumeration of available table shapes.
/// </summary>
public enum TableShapeType
{
    /// <summary>
    /// Rectangular table with rounded corners
    /// </summary>
    Rectangle,

    /// <summary>
    /// Square table with rounded corners
    /// </summary>
    Square,

    /// <summary>
    /// Circular table
    /// </summary>
    Round,

    /// <summary>
    /// Elliptical/oval table
    /// </summary>
    Oval
}
